int 	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while ((int)s1[i] != '\0' && (int)s2[i] != '\0')
	{
		if ((int)s1[i] != (int)s2[i])
			return ((int)s1[i] - (int)s2[i]);
		i++;
	}
	if (s1[i] != '\0')
		return ((int)s1[i]);
	if (s2[i] != '\0')
		return (-(int)s2[i]);
	return (0);
}
