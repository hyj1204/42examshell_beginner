char *ft_strpbrk(const char *s1, const char *s2)
{
	int i;
	int j;
	
	i = 0;
	j = 0;
	while (s1[i])
	{
		while (s2[j])
		{
			if (s2[j] == s1[i])
				return ((char*)&s1[i]);
			j++;
		}
		j = 0;
		i++;
	}
	return 0;
}
